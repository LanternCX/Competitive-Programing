#include <bits/stdc++.h>
using namespace std;
using ll = long long;
using ull = unsigned long long;
using pii = pair<int, int>;
void sol() {
	map<int, int> mp;
	for (auto &[a, b] : mp) {
		cout << a << ' ' << b << '\n';
	}
}
int main() {
	ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
	int t = 1;
	cin >> t;
	while (t--) {
		sol();
	}
	return 0;
}
